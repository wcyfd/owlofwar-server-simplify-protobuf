package com.randioo.owlofwar_server_simplify_protobuf.entity.po;

public class CardLevel {
	private int level;
	
	private int costMoney;
	
	private int costCard;

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public int getCostMoney() {
		return costMoney;
	}

	public void setCostMoney(int costMoney) {
		this.costMoney = costMoney;
	}

	public int getCostCard() {
		return costCard;
	}

	public void setCostCard(int costCard) {
		this.costCard = costCard;
	}
	
	
}
